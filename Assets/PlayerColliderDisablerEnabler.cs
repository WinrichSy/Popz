using UnityEngine;
using System.Collections;

public class PlayerColliderDisablerEnabler : MonoBehaviour {
	
	// Use this for initialization
	void Start () {
		var player = GameObject.Find ("Player");
		var player2DBoxCollider = player.GetComponent<BoxCollider2D>();
		player2DBoxCollider.enabled = false;

		var player2DCircleCollider = player.GetComponent<CircleCollider2D> ();
		player2DCircleCollider.enabled = true;

	}
	
	// Update is called once per frame
	void Update () {
	
	}
}
